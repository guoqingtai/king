package knight.c0_survey.entity;

public class BQuestion {
	
	private int qsId;
	
	private int order;
	
	private String text;

	public int getQsId() {
		return qsId;
	}

	public void setQsId(int qsId) {
		this.qsId = qsId;
	}

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
}
